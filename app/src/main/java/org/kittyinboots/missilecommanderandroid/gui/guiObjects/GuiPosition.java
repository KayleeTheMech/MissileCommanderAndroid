package org.kittyinboots.missilecommanderandroid.gui.guiObjects;


public class GuiPosition {
    private float x;
    private float y;

    public GuiPosition(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
